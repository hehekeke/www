            var sh=$(document).height()-$(window).height()-10;
            //console.log(sh)
            if($(window).scrollTop() >= sh && scr==0){
                $('.loading').show()
                i=i+1;
                //ajax
                $.ajax({
                    type:"POST",
                    url:"/service/ajaxCases",
                    data:{page:i},
                    datatype:"json",
                    success:function(a){
                        var data=$.parseJSON(a);
                        if(data.ret==0)
                        {
                            var data=data.data;
                            var html="";
                            $.each(data,function(i,data){
                                html+='<li>'+
                                    '<h3>'+data.title+'</h3>'+
                                    '<p class="date">'+data.add_date+'</p>'+
                                    '<img src="'+data.image+'" alt="'+data.title+'">'+
                                    '<p>'+data.des+'</p>'+
                                    '<div class="case_foot clearfix">'+
                                    '<span class="view_num fl">'+data.viewcount+'</span> <span class="down_num fl">'+data.downloadcount+'</span>'+
                                    '<a class="fr down_ppt" href="'+data.download+'">下载案例PPT</a>'+
                                    '<a class="fr viewlink" href="javascript:" data-id="'+data.id+'">查看案例</a>'+
                                    '</div>'+
                                    '</li>';
                            })
                            $('#case_list').append(html);
                            $('.loading').hide();
                        }
                        else
                        {
                            scr=1;
                            $('.loading').hide()
                            return false;
                        }
                    }
                })
            }
        });
        var case_num = 0;
        $("#edu_silder ul").width($('#edu_silder ul li').length*450);
        $("#edu_prev").css('opacity',0.1);
        function scroll_case(num){
            var nums = $('#edu_silder ul li').length;
            if(nums<1){return false};
            var scroll_w = -(num*450);
            $("#edu_silder ul").stop().animate({"margin-left":scroll_w+"px"})
        }

        $("#edu_prev").click(function(){
            $("#edu_next").css('opacity',0.8);
            case_num--;
            var nums = $('#edu_silder ul li').length;
            if(case_num<0){
                case_num = 0;
                $("#edu_prev").css('opacity',0.1);
                return false;
                //case_num = nums - 5;
            }
            scroll_case(case_num)
        })
        $("#edu_next").click(function(){
            $("#edu_prev").css('opacity',0.8);
            case_num++;
            var nums = $('#edu_silder ul li').length;
            if(case_num>(nums-1)){
                case_num = nums-1;
                $("#edu_next").css('opacity',0.1);
                return false;
                //case_num = 0;
            }
            scroll_case(case_num)
        })
        $('#case_list').delegate('.down_ppt','click',function(event) {
            $('#ppt_open').show();
            return false;
        })
        $('#case_list').delegate('.viewlink','click',function(event) {
            var id=$(this).attr('data-id');
            $.ajax({
                url: '/service/ajaxLink',
                type: 'POST',
                dataType: 'json',
                data: {case_id:id},
                success: function(data) {
                    if(data.ret==0){
                        $('#object_out').html(data.data);
                        $('#object').show();
                        var w=$('#object_out').width()/2;
                        var h=$('#object_out').height()/2;
                        $('#object_out').css({
                            "margin-left": -w,
                            "margin-top": -h
                        });
                    }else{
                        alert(data.msg)
                    }
                }
            });
        });
