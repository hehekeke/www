            <h1 class="title">“宁波市电子商务协会”正式与微巴达成战略合作</h1>
            <div class="article-info">
                <div class="bdsharebuttonbox clearfix"><span class="fl mr10">2015-04-14 02:16</span> <a href="#" class="bds_more" data-cmd="more"></a><a href="#" class="bds_qzone" data-cmd="qzone" title="分享到QQ空间"></a><a href="#" class="bds_weixin" data-cmd="weixin" title="分享到微信"></a><a href="#" class="bds_tsina" data-cmd="tsina" title="分享到新浪微博"></a><a href="#" class="bds_tqq" data-cmd="tqq" title="分享到腾讯微博"></a></div></div>
            <div class="show-content">
                <p style="text-align:center;">
                    <img src="/assets/js/kindeditor/attached/image/20150414/20150414062004_76936.png" alt="" />
                </p>
                <p style="text-align:left;">
                    <span style="font-size:14px;">4月13日，宁波市江东区电子商务协会成立大会暨互联网经济发展论坛在宁波举办,宁波江东区首个电子商务行业精英合作平台的成立。为了探索全新的电子商务发展模式，“宁波市电子商务协会”正式与微巴达成合作，微信分销模式成发展新趋势，并就如何将微商引入传统电商体系展开探讨。</span><span style="font-size:14px;"></span>
                </p>
                <p style="text-align:center;">
	<span style="font-size:14px;"><img src="/assets/js/kindeditor/attached/image/20150414/20150414062032_17398.png" alt="" /><br />
</span>
                </p>
                <p style="text-align:left;">
	<span style="font-size:14px;">大会现场，微巴（微信生意宝）首席讲师赵文旭给现场的各位商家讲了新时代环境下的电商业态、微商的现状、微信分销模式等内容。掀起现场商家的热烈探讨。<br />
</span>
                </p>
                <p style="text-align:center;">
	<span style="font-size:14px;"><img src="/assets/js/kindeditor/attached/image/20150414/20150414062107_34874.png" alt="" /><br />
</span>
                </p>
                <p style="text-align:left;">
	<span style="font-size:14px;">传统电商在移动互联网的冲击下正面临转型的档口，市场被数以百万计的微商争夺，销售利润又被淘宝天猫等平台方蚕食，大多都在勉强经营，大家都在寻求一条解困之路。而微巴人人店的微信分销模式，在微商的发展背景下已经具备时代趋势，传统电商可以借助微商的力量拓宽销售渠道和革新销售渠道及模式，人人店的一键分销模式势必将给传统电商迎来更大的革新和进步。<br />
</span>
                </p>
                <p style="text-align:center;">
	<span style="font-size:14px;"><img src="/assets/js/kindeditor/attached/image/20150414/20150414062137_60571.png" alt="" /><br />
</span>
                </p>
                <p style="text-align:left;">
	<span style="font-size:14px;">目前我国政府重视电子商务产业发展，正在完善电子商务的法律环境和网上的消费环境。今年两会上马化腾更是强调要大力发展微信电商，降低物流的成本和交易成本。从战略上提出要让“互联网+”落地的行动计划，并正从安全、支付等方面加大力度，促进微信电商的进一步发展。微信电商及微信营销必将给人们生活带来更大的发展和进步。<br />
</span>
                </p>			</div>
