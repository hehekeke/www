                $("#dls_next").css('opacity',0.8);
                case_num--;
                var nums = $('#dls_silder ul li').length;
                if(case_num<0){
                    case_num = 0;
                    $("#dls_prev").css('opacity',0.1);
                    return false;
                    //case_num = nums - 5;
                }
                scroll_case(case_num)
            })
            $("#dls_next").click(function(){
                $("#dls_prev").css('opacity',0.8);
                case_num++;
                var nums = $('#dls_silder ul li').length;
                if(case_num>(nums-3)){
                    case_num = nums-3;
                    $("#dls_next").css('opacity',0.1);
                    return false;
                    //case_num = 0;
                }
                scroll_case(case_num)
            })
