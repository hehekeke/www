                <h4>APP是什么？</h4>
                &nbsp;&nbsp;&nbsp;&nbsp;APP，即英文Application的简称，意思是应用程序。移动APP，是指针对手机、平板电脑等移动接入互联网的设备而开发的应用程序，简单地说，就是手机或平板电脑上安装的应用程序。<br />
                <h4>移动电子政务的发展史</h4>
                &nbsp;&nbsp;&nbsp;&nbsp; 随着移动技术、计算机技术和移动终端技术的发展，移动电子政务技术已经经历了三代。<br />
                &nbsp;&nbsp;&nbsp;&nbsp; 第一代是以短讯为基础的移动电子政务技术存在着许多严重的缺陷，其中最严重的问题是实时性较差，查询请求不会立即得到回答。此外，由于短讯信息长度的限制也使得一些查询无法得到一个完整的答案。这些令用户无法忍受的严重问题也导致了一些早期使用基于短讯的移动电子政务系统的部门纷纷要求升级和改造现有的系统。<br />
                &nbsp;&nbsp;&nbsp;&nbsp; 第二代移动电子政务系统采用基于WAP技术的方式，手机主要通过浏览器的方式来访问WAP网页，以实现信息的查询，部分地解决了第一代移动访问技术的问题。第二代的移动访问技术的缺陷主要表现在WAP网页访问的交互能力极差，因此极大地限制了移动电子政务系统的灵活性和方便性。此外，由于WAP使用的加密认证的WTLS协议建立的安全通道必须在WAP网关上终止，形成安全隐患，所以WAP网页访问的安全问题对于安性要求极为严格的政务系统来说也是一个严重的问题。这些问题也使得第二代技术难以满足用户的要求。<br />
                &nbsp;&nbsp;&nbsp;&nbsp; 新一代的移动电子政务系统，也就是第三代移动电子政务系统融合了3G移动技术、智能移动终端、VPN、数据库同步、身份认证及Webservice等多种移动通讯、信息处理和计算机网络的最新的前沿技术，以专网和无线通讯技术为依托，使得系统的安全性和交互能力有了极大的提高，为电子商务人员提供了一种安全、快速的现代化移动执法机制。数码星辰的移动电子政务软件是新一代移动电子政务系统的典型的代表。它采用了先的的自适应结构可以灵活地适应用户的数据环境，具有现场零编程、高安全、部署快、使用方便、响应速度快的优点。该系统支持GPRS、CDMA、Edge以及所有制式的3G网络。<br />
                <h4>移动电子政务目前的进展状况</h4>
                &nbsp;&nbsp;&nbsp;&nbsp;随着3G网络覆盖率、智能移动终端普及率的提高，移动电子政务成为电子政务的发展趋势。APP已经渗透生活的每一个角落。越来越多的一线城市、地方政府开始移动电子政务，通过开发政务APP应用，实现独立业务流的小、快、多的高效政务办公模式。<br />
                &nbsp;&nbsp;&nbsp;&nbsp;在全国，已有不少政府部门也试水移动互联网，市民只需点击手机，就能轻松获取公共服务信息。在江阴、深圳、佛山、昆山等地方的政府部门已经在试水移动互联网，市民只需点击手机，就能轻松获取公共服务信息。而通过政务APP，这些地方的政府部门正试图将服务延伸到更大的人群。中国软件评测中心一年一度的《中国政府网站绩效评估总报告》日前已经出炉，值得注意的是，报告今年首次引入对移动政务APP的专项评估，其中北京以总分80分位列各地区之首。为此，报告首次增加了对移动政务APP的专项评估，截至2013年11月15日，部委、省级、地市和区县政府的移动政务客户端建成比例分别为25%、31%、11%和3.8%。<br />
